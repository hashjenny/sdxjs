// 哈希值只有2位，对四个文件进行哈希，哈希冲突实际概率是多少

/*
计算冲突概率的最简单方法是先计算完全没有发生任何冲突的概率，然后用1减去这个概率。

第一个文件：可以哈希到4个值中的任意一个，不会与任何文件冲突（因为还没有其他文件）。概率为 4/4 = 1。
第二个文件：为了不冲突，它必须哈希到剩下的3个未被第一个文件占用的值。概率为 3/4。
第三个文件：为了不冲突，它必须哈希到剩下的2个未被前两个文件占用的值。概率为 2/4。
第四个文件：为了不冲突，它必须哈希到最后1个未被前三个文件占用的值。概率为 1/4。

因此，所有四个文件哈希值都唯一的概率（即无冲突概率 P(无冲突)）为：
P(无冲突) = 1 * (3/4) * (2/4) * (1/4) = 6/64 = 3/32
P(冲突) = 1 - P(无冲突) = 1 - (3/32) = 29/32
*/